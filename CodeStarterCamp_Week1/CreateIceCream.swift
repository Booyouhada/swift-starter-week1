import Foundation

// MARK: - STEP 2 아이스크림 그리기

/// 아이스크림 부분 출력
func iceCreamPartDraw(count: Int) {
    for _ in 0..<count {
        print("***********")
    }
}

/// 막대 부분 출력
func stickPartDraw(count: Int) {
    for _ in 0..<count {
        print("    | |")
    }
}

