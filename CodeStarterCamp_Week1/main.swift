func makeIcecream(size: Int) {
    for _ in 1...size{
        print( "***********")
    }
}

makeIcecream(size: 10)

func makeStick(size: Int) {
    for _ in 1...size{
        print("    | |")
    }
}
makeStick(size: 4)


