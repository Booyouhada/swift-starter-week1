import Foundation

func cookIceCream() {
    for _ in 1...8 {
        for _ in 1...11 {
            print("*", terminator: "")
        }
        print()
    }
}

func cookIceCreamBar() {
    for _ in 1...4 {
        print("    | |")
    }
}

